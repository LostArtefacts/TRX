void gar_RotYXZsuperpack(int16_t** pprot, int skip)
{
    uint16_t* prot;
    int32_t packed;

    /* Can skip over rots to get to one we want */
    while (skip) {
        prot = (uint16_t*)*pprot;
        if (*prot & (3 << 14))
            (*pprot) += 1;
        else
            (*pprot) += 2;
        skip--;
    }

    /* Check top bits; 0 -> YXZpack, 1 -> X, 2 -> y, 3 -> Z
            Returns if 1 or 2 words to be skipped */
    prot = (uint16_t*)*pprot;
    switch (*prot >> 14) {
    case 0:
        packed = (*prot << 16) + *(prot + 1);
        phd_RotYXZpack(packed);
        (*pprot) += 2;
        return;
    case 1:
        phd_RotX((int16_t)((*prot & 1023) << 6));
        break;
    case 2:
        phd_RotY((int16_t)((*prot & 1023) << 6));
        break;
    default:
        phd_RotZ((int16_t)((*prot & 1023) << 6));
        break;
    }

    (*pprot) += 1;
}

